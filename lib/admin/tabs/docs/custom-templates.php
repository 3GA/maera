      <p>Since Maera uses <a href="http://twig.sensiolabs.org/">twig</a> we were able to do some fancy stuff, allowing you to include your own templates in plugins. The templates reside in the <code>views</code> folder.</p>

<p>You can see the default views in the <a href="https://github.com/presscodes/maera/tree/master/views">theme's views folder</a></p>

<p>Of course you can customize that, and build a different structure for your own shell.  However, this is the default structure that will be used if you only choose to customize a couple of these files.</p>

<p>Maera will automatically look for a <code>/views</code> folder first in the folder of your shell, and if it doesn't find one of the required files in there, it will automatically fallback to the default template views from the theme's views folder.</p>

